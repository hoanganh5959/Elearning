    <script>
        const lessonsData = <?php echo $lessons_json; ?>;
        document.addEventListener("DOMContentLoaded", () => {
            const listDiv = document.getElementById("lessonList");
            let mainVideo = document.getElementById("mainVideo");

            lessonsData.forEach((lesson, index) => {
                const div = document.createElement("div");
                div.classList.add("lesson-item");
                if (index === 0) {
                    div.classList.add("active-lesson");
                    mainVideo.src = `https://www.youtube.com/embed/${lesson.youtube_id}`;
                }

                div.textContent = `${index + 1}. ${lesson.title}`;
                div.dataset.videoId = lesson.youtube_id;

                div.addEventListener("click", () => {
                    document.querySelectorAll(".lesson-item").forEach(el => el.classList.remove("active-lesson"));
                    div.classList.add("active-lesson");
                    mainVideo.src = `https://www.youtube.com/embed/${div.dataset.videoId}`;
                });

                listDiv.appendChild(div);
            });
        });
    </script>

